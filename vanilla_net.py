import torch
import torch.nn as nn


class encoder_decoder:
    vgg_backend = nn.Sequential(
        nn.Conv2d(3, 3, (1, 1)),
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(3, 64, (3, 3)),
        nn.ReLU(),  # relu1-1
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(64, 64, (3, 3)),
        nn.ReLU(),  # relu1-2
        nn.MaxPool2d((2, 2), (2, 2), (0, 0), ceil_mode=True),
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(64, 128, (3, 3)),
        nn.ReLU(),  # relu2-1
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(128, 128, (3, 3)),
        nn.ReLU(),  # relu2-2
        nn.MaxPool2d((2, 2), (2, 2), (0, 0), ceil_mode=True),
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(128, 256, (3, 3)),
        nn.ReLU(),  # relu3-1
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(256, 256, (3, 3)),
        nn.ReLU(),  # relu3-2
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(256, 256, (3, 3)),
        nn.ReLU(),  # relu3-3
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(256, 256, (3, 3)),
        nn.ReLU(),  # relu3-4
        nn.MaxPool2d((2, 2), (2, 2), (0, 0), ceil_mode=True),
        nn.ReflectionPad2d((1, 1, 1, 1)),
        nn.Conv2d(256, 512, (3, 3)),
        nn.ReLU()  # relu4-1, this is the last layer used
    )

    # Frontend for vanilla model
    frontend = nn.Sequential(
        nn.Linear(512 * 4 * 4, 4096),
        nn.Linear(4096, 256),
        nn.Linear(256, 100)
    )


class Model(nn.Module):
    def __init__(self, num_classes=100):
        super(Model, self).__init__()
        self.backend = encoder_decoder.vgg_backend

        self.frontend = encoder_decoder.frontend

        # Keep encoder weights fixed (if you want to)
        for param in self.backend.parameters():
            param.requires_grad = False

        self.mse_loss = nn.MSELoss()

    def forward(self, X):
        enc = self.backend(X)
        enc_flatten = enc.view(enc.size(0), -1)
        dec = self.frontend(enc_flatten)
        return dec

